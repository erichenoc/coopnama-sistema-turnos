// Sidebar footer with user profile and logout
function SidebarFooter() {
  const { user } = useOrg()
  const displayName = user?.full_name || 'Admin COOPNAMA'
  const displayRole = user?.role || 'admin'

  return (
    <div className="flex items-center gap-3">
      <Avatar name={displayName} status="online" size="sm" />
      <div className="flex-1 min-w-0">
        <p className="text-sm font-medium text-gray-700 truncate">{displayName}</p>
        <p className="text-xs text-gray-500 truncate capitalize">{displayRole}</p>
      </div>
      <button
        onClick={() => logoutAction()}
        className="p-1.5 text-gray-400 hover:text-coopnama-danger rounded transition-colors"
        title="Cerrar sesion"
      >
        <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
        </svg>
      </button>
    </div>
  )
}

// Branch switcher for multi-branch admins
function BranchSwitcher() {
  const { branches, branchId, switchBranch } = useOrg()

  if (branches.length <= 1) return null

  return (
    <select
      value={branchId}
      onChange={(e) => switchBranch(e.target.value)}
      className="px-3 py-1.5 text-sm bg-neu-bg shadow-neu-xs rounded-neu-sm text-gray-700 focus:outline-none focus:ring-2 focus:ring-coopnama-primary/30"
    >
      <option value="all">Todas las sucursales</option>
      {branches.map((b) => (
        <option key={b.id} value={b.id}>{b.name}</option>
      ))}
    </select>
  )
}

export default function MainLayout({
  children,
}: {
  children: React.ReactNode
}) {
  const { user } = useOrg()

  return (
    <DashboardLayout
      navItems={navItems}
      sidebarFooter={<SidebarFooter />}
      headerProps={{
        user: {
          name: user?.full_name || 'Admin COOPNAMA',
          role: user?.role || 'Administrador',
        },
        actions: <BranchSwitcher />,
      }}
    >
      {children}
    </DashboardLayout>
  )
}
